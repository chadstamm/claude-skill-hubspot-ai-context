def load_brand() -> SimpleNamespace:
    override_path = Path.cwd() / "brand-override.local.py"
    if not override_path.exists():
        return DEFAULT_BRAND
    spec = importlib.util.spec_from_file_location("brand_override", override_path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    brand = SimpleNamespace(**vars(DEFAULT_BRAND))
    for key in vars(DEFAULT_BRAND):
        if hasattr(module, key):
            value = getattr(module, key)
            if value is not None:
                setattr(brand, key, value)
    return brand


def esc(value) -> str:
    if value is None:
        return ""
    return html.escape(str(value))


def attr(value) -> str:
    return html.escape(str(value or ""), quote=True)


def slugify(text: str) -> str:
    s = text.lower()
    s = re.sub(r"[^a-z0-9 \-]", "", s)
    s = s.replace(" ", "-")
    return re.sub(r"-+", "-", s).strip("-")


def split_items(value: str) -> list:
    """Split a multi-value field. Prefer semicolon when present, fall back to comma."""
    s = (value or "").replace("\n", " ").strip()
    if not s:
        return []
    sep = ";" if ";" in s else ","
    items = [t.strip() for t in s.split(sep)]
    return [t for t in items if t]


def render_pill_field(label: str, value: str) -> str:
    items = split_items(value)
    if not items:
        return ""
    pills_html = "".join(
        f'<button class="pill" data-copy="{attr(i)}" title="Click to copy">'
        f'<span class="pill-text">{esc(i)}</span>'
        f'<span class="pill-icon" aria-hidden="true">⧉</span>'
        f'</button>'
        for i in items
    )
    join_str = "; ".join(items)
    return f"""
<div class="field">
  <div class="field-label">{esc(label)}</div>
  <div class="field-hint">Click any tag to copy it individually, or use Copy all below.</div>
  <div class="pills">{pills_html}</div>
  <button class="copy-all" data-copy="{attr(join_str)}">Copy all ({len(items)})</button>
</div>"""


def render_semicolon_text(label: str, value: str) -> str:
    items = split_items(value)
    if not items:
        return ""
    joined = "; ".join(items)
    return f"""
<div class="field">
  <div class="field-label">{esc(label)}</div>
  <div class="field-value"><p>{esc(joined)}</p></div>
  <button class="copy-btn" data-copy="{attr(joined)}">Copy</button>
</div>"""


def render_text(label: str, value: str) -> str:
    if not value:
        return ""
    # Preserve paragraph breaks for "Other" multi-paragraph content
    paras = re.split(r"\n\s*\n", value.strip())
    paras_html = "".join(f"<p>{esc(p)}</p>" for p in paras if p)
    return f"""
<div class="field">
  <div class="field-label">{esc(label)}</div>
  <div class="field-value">{paras_html}</div>
  <button class="copy-btn" data-copy="{attr(value)}">Copy</button>
</div>"""


def render_field(label: str, value: str) -> str:
    if not value:
        return ""
    if label in PILL_FIELDS:
        return render_pill_field(label, value)
    if label in SEMICOLON_TEXT_FIELDS:
        return render_semicolon_text(label, value)
    return render_text(label, value)


def render_icp_card(icp: dict, num: int) -> str:
    name = icp.get("Name", "Unnamed").strip()
    category = icp.get("Category", "").strip()
    grouping = icp.get("Grouping", "").strip()
    slug = icp.get("Slug", "").strip() or slugify(name)
    fields_html = "\n".join(render_field(label, icp.get(label, "")) for label in FIELD_ORDER)

    # Copy-all-fields for the whole ICP
    parts = []
    for label in FIELD_ORDER:
        v = (icp.get(label) or "").strip()
        if not v:
            continue
        if label in (PILL_FIELDS | SEMICOLON_TEXT_FIELDS):
            v = "; ".join(split_items(v))
        parts.append(f"=== {label.upper()} ===\n{v}")
    copy_all_text = "\n\n".join(parts)

    meta = " · ".join(p for p in [grouping, category] if p)

    return f"""
<article class="icp" id="{esc(slug)}">
  <div class="icp-header">
    <div class="icp-num">{num}</div>
    <div>
      <h2>{esc(name)}</h2>
      <div class="icp-meta">{esc(meta)}</div>
    </div>
    <button class="copy-all" data-copy="{attr(copy_all_text)}">Copy all fields</button>
  </div>
  {fields_html}
</article>"""


