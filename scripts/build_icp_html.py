def main():
    parser = argparse.ArgumentParser(description="Build HubSpot ICP paste sheet HTML from CSV")
    parser.add_argument("csv_path", help="Path to ICP CSV file")
    parser.add_argument("output_path", nargs="?", help="Output HTML path (defaults to alongside CSV)")
    args = parser.parse_args()

    csv_path = Path(args.csv_path)
    if not csv_path.exists():
        print(f"Error: CSV not found: {csv_path}", file=sys.stderr)
        sys.exit(1)

    with csv_path.open() as f:
        rows = list(csv.DictReader(f))
    if not rows:
        print(f"Error: CSV is empty: {csv_path}", file=sys.stderr)
        sys.exit(1)

    if args.output_path:
        out_path = Path(args.output_path)
    else:
        out_path = csv_path.parent / f"{csv_path.stem}-icps.html"

    brand = load_brand()
    html_str = build_html(rows, brand, csv_path.stem)
    out_path.write_text(html_str)
    print(f"Wrote {out_path} ({out_path.stat().st_size:,} bytes, {len(rows)} ICPs)")


if __name__ == "__main__":
    main()
